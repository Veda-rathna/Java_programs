package Variables;
class Variables{
    public static void main(String args[]){
        
       //Variables
       int x=10;                        //int
       long y = 112233445566778899L;    //long (end with 'L')
       float z = 1.23f;                 //float (end with 'f')
       double d = 1.23456789012;        //double
       int a = 10, b = 20;
       boolean equal = (a == b);        //boolean values(true or false)
       System.out.println(x);
       System.out.println(y);
       System.out.println(z);
       System.out.println(d);
       System.out.println(equal);
    }
}